package hintDTO;

public class hintpack {
	
	int correct;
	int i; 
	int life;
	int score;
	
	public hintpack(int correct, int i, int life, int score) {
		this.correct = correct;
		this.i = i;
		this.life = life;
		this.score = score;
	}
	

	public int getScore() {
		return score;
	}


	public int getCorrect() {
		return correct;
	}


	public int getI() {
		return i;
	}


	public int getLife() {
		return life;
	}

	
	
	
	

}
